def f(ui, temperature):
    return 1 / (1 + math.exp(-(ui/temperature)))

def countUi(x, wi, thetai):
    x_copy = deepcopy(x)
    suma = 0
    for j in range(len(x)):
        suma += wi[j] * x[j]
    suma -= thetai
    return suma

def countNextXi(ui, temperature, betai):
    valueToCompare = f(ui, temperature)
    #print("Value: ")
    #print(valueToCompare)
    if valueToCompare >= betai:
        return 1
