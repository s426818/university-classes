def  countUI(w, x, index, theta):
    u = -theta[index]
    currentW = w[index]
    for j in range(25):
        u += currentW[j] * x[j]
    return u

def countU(w, x, theta):
    u = []
    for i in range(25):
        u.append(countUI(w, x, i, theta))
    return u

def countNewX(x, u):
    x_new = deepcopy(x)
    for i in range(len(x_new)):
        if(u[i] < 0):
            x_new[i] = 0
        elif (u[i] == 0):
            # continue
            x_new[i] = x[i]
        else:
            x_new[i] = 1
    return x_new

def countC(z):
    z_copy = deepcopy(z)
    c = []
    for i in range(25):
        temp = []
        for j in range(25):
            if not(i == j):
                temp.append((z_copy[i] - 0.5) * (z_copy[j] - 0.5))
            else:
                temp.append(0)
        c.append(temp)
    return c

